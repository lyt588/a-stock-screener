def _ensure_v4_cols(df: pd.DataFrame) -> pd.DataFrame:
    """
    确保 V4 stocks DataFrame 包含所有必需列。
    缺失列用 _V4_STOCKS_DEFAULTS 中的默认值填充，不修改已有列。
    空 DataFrame 直接原样返回。
    """
    if df is None or df.empty:
        return df if df is not None else pd.DataFrame()
    df = df.copy()
    for col, default in _V4_STOCKS_DEFAULTS.items():
        if col not in df.columns:
            df[col] = default
    return df


# ── V4 全量分析主函数 ────────────────────────────────────────────────────

def run_v4_analysis(
    result_df: pd.DataFrame,
    rt_df: pd.DataFrame | None = None,
) -> dict:
    """
    V4 全量分析入口。
    返回包含情绪数据、各类候选池、逐股详情的完整报告 dict。
    任何单步失败不影响其他步骤。
    """
    if result_df is None or result_df.empty:
        return {"error": "result_df 为空，请先运行实时选股"}

    try:
        # 0. 基础数据准备
        base_df   = rt_df if (rt_df is not None and not rt_df.empty) else result_df
        strengths = compute_sector_strength(base_df)

        # 1. 市场情绪
        max_c = int(result_df["consec_limit"].max() or 0) \
                if "consec_limit" in result_df.columns else 0
        emotion = compute_market_emotion_v4(base_df, result_df, max_c)

        # 2. 逐股评分
        rows: list = []
        for _, row in result_df.iterrows():
            try:
                code   = str(row.get("code", ""))
                sector = get_sector(code)
                rank   = strengths.get(sector, 0.5)

                ldr  = compute_leader_score_v4(row, sector_rank=rank)
                auct = compute_auction_v4(row, emotion_total=emotion["total"])
                rsk  = compute_risk_score_v4(row, emotion_total=emotion["total"])
                adv  = compute_advanced_scores(row, sector_strength=rank)

                # 把 adv 子分标准化到 0-100 供综合评分使用
                comp = compute_final_composite_v4(
                    leader_score  = ldr["leader_score"],
                    sector_score  = adv["sector_score"] * 10,   # 0-10 → scale
                    vol_score     = adv["vol_score"]    * 4,    # 0-25 → scale
                    pop_score     = adv["pop_score"]    * 4,    # 0-25 → scale
                    auction_score = auct["auction_score"],
                    risk_score    = rsk["risk_score"],
                    emotion_total = emotion["total"],
                )

                buy_r  = _gen_buy_reasons(row, ldr, adv)
                risk_r = _gen_risk_reasons(row, rsk)

                rows.append({
                    "code":           code,
                    "name":           str(row.get("name", "")),
                    "sector":         sector,
                    "pct_chg":        float(row.get("pct_chg", 0) or 0),
                    "consec":         int(row.get("consec_limit", 0) or 0),
                    "amount":         float(row.get("amount", 0) or 0),
                    # 龙头
                    "leader_score":   ldr["leader_score"],
                    "leader_grade":   ldr["leader_grade"],
                    "leader_badge":   ldr["leader_badge"],
                    "true_leader":    ldr["true_leader"],
                    "tl_confidence":  ldr["tl_confidence"],
                    # 5维度
                    "trend_score":    adv["trend_score"],
                    "vol_score":      adv["vol_score"],
                    "pop_score":      adv["pop_score"],
                    "sector_score":   adv["sector_score"],
                    "risk_adv_score": adv["risk_score"],
                    # 竞价
                    "auction_score":  auct["auction_score"],
                    "wts_grade":      auct["wts_grade"],
                    "watchable":      auct["watchable"],
                    "auction_pct":    auct["auction_pct"],
                    "vol_ratio_pct":  auct["vol_ratio_pct"],
                    # 风险
                    "risk_v4_score":  rsk["risk_score"],
                    "risk_v4_level":  rsk["risk_level"],
                    "risk_v4_color":  rsk["risk_color"],
                    "auto_degrade":   rsk["auto_degrade"],
                    # 综合
                    "composite_score":  comp["composite_score"],
                    "recommend_grade":  comp["recommend_grade"],
                    "recommend_color":  comp["recommend_color"],
                    # 理由
                    "buy_reasons":   "；".join(buy_r)  if buy_r  else "—",
                    "risk_reasons":  "；".join(risk_r) if risk_r else "—",
                })
            except Exception:
                continue

        if not rows:
            return {"emotion": emotion, "stocks": pd.DataFrame(),
                    "sector_strengths": strengths, "top_sector": "—"}

        stocks_df = (
            pd.DataFrame(rows)
            .sort_values("composite_score", ascending=False)
            .reset_index(drop=True)
        )
        stocks_df.index += 1

        # 补全所有必需列（防止后续 Tab 访问缺失列）
        stocks_df = _ensure_v4_cols(stocks_df)

        # 3. 分类候选池（基于补全后的 DataFrame，安全访问列）
        leader_pool    = _ensure_v4_cols(
            stocks_df[stocks_df["leader_score"] >= 60].copy()
        )
        wts_pool       = _ensure_v4_cols(
            stocks_df[stocks_df["wts_grade"].isin(["强", "中"])].copy()
        )
        high_risk_pool = _ensure_v4_cols(
            stocks_df[stocks_df["auto_degrade"] == True].copy()
        )

        top_sector = max(strengths, key=strengths.get) if strengths else "—"

        return {
            "emotion":          emotion,
            "stocks":           stocks_df,
            "leader_pool":      leader_pool,
            "wts_pool":         wts_pool,
            "high_risk_pool":   high_risk_pool,
            "top_sector":       top_sector,
            "sector_strengths": strengths,
        }
    except Exception as exc:
        return {"error": str(exc)}


