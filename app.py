# ── 游资行为规则识别 ──────────────────────────────────────────────────────

def identify_yt_behavior(
    df: pd.DataFrame,
    sector_heat_df: pd.DataFrame | None = None,
) -> pd.DataFrame:
    """
    规则版游资行为识别：
      高换手(>8%) + 高成交额(>2亿) + 涨停/连板 + 板块强 = 游资活跃
    行为标签：涨停敢死队 / 连板龙头 / 板块联动 / 游资集中 / 主力拉升
    """
    if df.empty:
        return pd.DataFrame()

    hot_sectors: set = set()
    if sector_heat_df is not None and not sector_heat_df.empty:
        if "板块" in sector_heat_df.columns:
            hot_sectors = set(sector_heat_df.head(2)["板块"].tolist())

    records: list = []
    for _, row in df.iterrows():
        try:
            code      = str(row.get("code", ""))
            name      = str(row.get("name", ""))
            pct       = float(row.get("pct_chg",    0) or 0)
            turnover  = float(row.get("turnover",   0) or 0)
            amount    = float(row.get("amount",     0) or 0)
            is_limit  = bool(row.get("is_limit",   False))
            consec    = int(row.get("consec_limit", 0) or 0)
            vol_ratio = float(row.get("vol_ratio") or 0)
            sector    = get_sector(code)

            in_hot = sector in hot_sectors
            c1 = turnover >= 8       # 高换手
            c2 = amount   >= 2e8     # 高成交额
            c3 = is_limit or consec >= 1   # 涨停/连板
            c4 = in_hot              # 板块强

            met = sum([c1, c2, c3, c4])

            tags: list = []
            if is_limit and vol_ratio >= 2:           tags.append("涨停敢死队")
            if consec >= 2 and amount >= 3e8:          tags.append("连板龙头")
            if in_hot and is_limit:                    tags.append("板块联动")
            if turnover >= 15 and pct >= 5:            tags.append("游资集中")
            if not tags and pct >= 7:                  tags.append("主力拉升")

            if met >= 4:    label, lc = "游资活跃", "🔴"
            elif met >= 3:  label, lc = "疑似游资", "🟠"
            elif met >= 2:  label, lc = "主力参与", "🟡"
            else:           label, lc = "普通交易", "⚪"

            records.append({
                "代码":     code,
                "名称":     name,
                "涨跌幅":   f"{pct:+.2f}%",
                "换手率":   f"{turnover:.1f}%",
                "成交额":   f"{amount/1e8:.2f}亿",
                "量比":     f"{vol_ratio:.2f}x" if vol_ratio else "-",
                "板块":     sector,
                "热点板块": "🔥" if in_hot else "",
                "涨停":     "✅" if is_limit else "—",
                "连板数":   f"{consec}板" if consec else "—",
                "行为标签": "、".join(tags) if tags else "—",
                "游资识别": f"{lc} {label}",
                "_sort":    met * 10 + turnover * 0.5,
            })
        except Exception:
            continue

    if not records:
        return pd.DataFrame()
    result = (
        pd.DataFrame(records)
        .sort_values("_sort", ascending=False)
        .drop(columns=["_sort"])
        .reset_index(drop=True)
    )
    result.index += 1
    return result


