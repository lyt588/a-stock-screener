# ── 板块识别 ──────────────────────────────────────────────────────────────

def get_board(code: str) -> str:
    if code.startswith(("300", "301")): return "创业板"
    if code.startswith(("688", "689")): return "科创板"
    if code.startswith("8"):            return "北交所"
    return "主板"


def get_sector(code: str) -> str:
    if code.startswith(("300", "301")): return "创业板"
    if code.startswith(("688", "689")): return "科创板"
    if code.startswith("8"):            return "北交所"
    if code.startswith("6"):            return "上证主板"
    return "深证主板"


def limit_threshold(code: str) -> float:
    b = get_board(code)
    if b in ("创业板", "科创板"): return 19.5
    if b == "北交所":             return 29.5
    return 9.8


def _consec_detect_rate(board: str) -> float:
    if board in ("创业板", "科创板"): return 0.190
    if board == "北交所":             return 0.284
    return 0.095


