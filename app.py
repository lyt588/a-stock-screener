# ── 筛选主流程（供 Tab1 / Tab4 复用）────────────────────────────────────

def run_screening(
    codes, pct_chg_min, amount_min, price_lo, price_hi, turnover_min,
    vol_ratio_min, ret3_min, consec_min,
    use_limit_up, use_heavy_vol, use_ma_bull, use_new_high,
    sort_by, max_results,
    screen_mode: str = "标准",
):
    # 宽松模式强制跳过历史K线
    force_no_hist = SCREEN_MODE_CONFIG.get(screen_mode, {}).get("force_no_hist", False)

    need_hist = False if force_no_hist else (
        vol_ratio_min > 0
        or ret3_min > 0
        or int(consec_min) > 0
        or any([use_limit_up, use_heavy_vol, use_ma_bull, use_new_high])
        or sort_by in HIST_REQUIRED_SORTS
    )

    with st.status("正在筛选…", expanded=True) as status:
        stock_dict = get_stock_list()
        codes_all  = codes if codes else list(stock_dict.keys())
        # 读取模块级诊断变量（由 get_stock_list 更新）
        _pool_src  = _POOL_INFO.get("source", "未知")
        _pool_warn = _POOL_INFO.get("warnings", [])
        st.session_state.pool_size    = len(codes_all)
        st.session_state.pool_source  = _pool_src
        st.session_state.pool_log     = _POOL_INFO.get("log", [])
        st.session_state.pool_break   = _POOL_INFO.get("breakdown", {})
        if len(codes_all) < 4000:
            st.warning(
                f"⚠️ 沪深A股池仅 **{len(codes_all)}** 只（期望≥4000）。来源：{_pool_src}。"
                "建议点击侧边栏「🔄 强制刷新股票池」重新拉取完整列表。"
            )
        for _w in _pool_warn:
            st.warning(_w)
        st.write(f"✅ 股票列表：{_pool_src}，共 **{len(codes_all)}** 只")

        st.write(f"正在获取腾讯实时行情（{len(codes_all)} 只，每批 {BATCH_SIZE}）…")
        try:
            realtime_df = fetch_all_realtime(tuple(codes_all))
        except requests.exceptions.ConnectionError:
            status.update(label="网络连接失败 ❌", state="error")
            st.error("⚠️ 无法连接腾讯行情接口（qt.gtimg.cn），请检查网络后重试。")
            return pd.DataFrame(), need_hist
        except requests.exceptions.Timeout:
            status.update(label="请求超时 ❌", state="error")
            st.error("⚠️ 腾讯行情接口超时，请稍后重试。")
            return pd.DataFrame(), need_hist
        except Exception as e:
            status.update(label="行情获取失败 ❌", state="error")
            st.error(f"⚠️ 获取行情失败：{e}")
            return pd.DataFrame(), need_hist

        if realtime_df.empty:
            status.update(label="行情数据为空 ❌", state="error")
            st.error(
                "⚠️ 腾讯接口返回数据为空，可能原因：\n"
                "- 当前为非交易时段\n- 网络或接口异常，请稍后重试"
            )
            return pd.DataFrame(), need_hist

        st.session_state.rt_count = len(realtime_df)
        st.write(f"✅ 实时行情：**{len(realtime_df)}** 只有效数据")

        candidates = apply_basic_filters(
            realtime_df, pct_chg_min, amount_min * 1e8, price_lo, price_hi, turnover_min,
        )
        st.session_state.filter_count = len(candidates)
        st.write(f"✅ 基础初筛通过 **{len(candidates)}** 只（涨幅/成交额/价格/换手率）")

        if candidates.empty:
            status.update(label="初筛无结果", state="error")
            st.warning("没有股票通过初筛，请尝试放宽条件，或确认今日行情数据已更新。")
            return pd.DataFrame(), need_hist

        if need_hist:
            n_cands = len(candidates)
            if n_cands > HIST_CANDIDATE_CAP:
                candidates = (
                    candidates.sort_values("pct_chg", ascending=False)
                    .head(HIST_CANDIDATE_CAP).reset_index(drop=True)
                )
                st.warning(
                    f"⚠️ 候选 {n_cands} 只，超过历史获取上限 {HIST_CANDIDATE_CAP}，"
                    f"已自动取涨幅最高的前 {HIST_CANDIDATE_CAP} 只进行深度分析。"
                )
                n_cands = HIST_CANDIDATE_CAP
            else:
                est_sec = n_cands * 2
                st.write(
                    f"正在获取历史K线数据，共 **{n_cands}** 只"
                    f"（预计 {est_sec}–{est_sec + 10} 秒）…"
                )

            progress  = st.progress(0, text="历史数据加载中…")
            sigs_list = []
            for i, (_, row) in enumerate(candidates.iterrows()):
                try:
                    hist = fetch_hist_indicators(row["code"])
                except Exception:
                    hist = {}
                try:
                    sigs = compute_yt_signals(row, hist)
                except Exception:
                    sigs = {
                        "is_limit": False, "is_heavy": False, "is_ma_bull": False,
                        "is_ret3": False, "is_new_high": False, "consec_limit": 0,
                        "ma20": None, "ma60": None, "ret3": None,
                        "vol_ratio": None, "yt_score": 0,
                    }
                sigs_list.append(sigs)
                progress.progress(
                    (i + 1) / n_cands,
                    text=f"{row['code']}（{i+1}/{n_cands}）",
                )
            progress.empty()

            sigs_df    = pd.DataFrame(sigs_list)
            candidates = pd.concat(
                [candidates.reset_index(drop=True), sigs_df.reset_index(drop=True)],
                axis=1,
            )

            mask = pd.Series([True] * len(candidates), index=candidates.index)
            if use_limit_up:       mask &= candidates["is_limit"].fillna(False)
            if use_heavy_vol:      mask &= candidates["is_heavy"].fillna(False)
            if use_ma_bull:        mask &= candidates["is_ma_bull"].fillna(False)
            if use_new_high:       mask &= candidates["is_new_high"].fillna(False)
            if vol_ratio_min > 0:  mask &= candidates["vol_ratio"].fillna(0) >= vol_ratio_min
            if ret3_min > 0:       mask &= candidates["ret3"].fillna(-9999) >= ret3_min
            if int(consec_min) > 0:
                mask &= candidates["consec_limit"].fillna(0) >= int(consec_min)

            candidates = candidates[mask].reset_index(drop=True)
            st.write(f"✅ 游资深度筛选通过 **{len(candidates)}** 只")

        status.update(label="筛选完成 ✅", state="complete", expanded=False)

    if candidates.empty:
        return pd.DataFrame(), need_hist

    sort_col = SORT_COL_MAP.get(sort_by, "pct_chg")
    if sort_col not in candidates.columns:
        sort_col = "pct_chg"

    result = (
        candidates
        .sort_values(sort_col, ascending=False, na_position="last")
        .head(int(max_results))
        .reset_index(drop=True)
    )
    return result, need_hist


# ── 展示表格构建 ──────────────────────────────────────────────────────────

def build_display_df(result_df: pd.DataFrame, need_hist: bool) -> pd.DataFrame:
    def _yn(v):
        return "✅" if v else "—"

    def _fv(x, fmt=""):
        if x is None or (isinstance(x, float) and pd.isna(x)):
            return "-"
        return f"{x:{fmt}}" if fmt else str(x)

    disp = pd.DataFrame(index=result_df.index)
    disp["股票代码"]   = result_df["code"]
    disp["股票名称"]   = result_df["name"]
    disp["最新价(元)"] = result_df["price"].map(lambda x: f"{x:.2f}")
    disp["涨跌幅"]     = result_df["pct_chg"].map(lambda x: f"{x:+.2f}%")
    disp["成交额"]     = result_df["amount"].map(lambda x: f"{x/1e8:.2f} 亿")
    disp["成交量"]     = result_df["volume"].map(
        lambda x: f"{x/10000:.1f} 万手" if pd.notna(x) and x >= 10000
                  else (f"{x:.0f} 手" if pd.notna(x) else "-")
    )
    disp["换手率"]     = result_df["turnover"].map(
        lambda x: f"{x:.2f}%" if pd.notna(x) else "-"
    )

    if need_hist and "yt_score" in result_df.columns:
        disp.insert(2, "游资评分", result_df["yt_score"])
        disp["量比"]      = result_df["vol_ratio"].map(
            lambda x: f"{x:.2f}x" if pd.notna(x) else "-"
        )
        disp["MA20"]      = result_df["ma20"].map(lambda x: _fv(x, ".2f"))
        disp["MA60"]      = result_df["ma60"].map(lambda x: _fv(x, ".2f"))
        disp["MA多头"]    = result_df["is_ma_bull"].map(_yn)
        disp["近3日涨幅"] = result_df["ret3"].map(
            lambda x: f"{x:+.2f}%" if pd.notna(x) else "-"
        )
        disp["是否放量"]  = result_df["is_heavy"].map(_yn)
        disp["是否涨停"]  = result_df["is_limit"].map(_yn)
        disp["连板数"]    = result_df["consec_limit"].map(
            lambda x: f"{int(x)}板" if pd.notna(x) and x > 0 else "—"
        )
        disp["60日新高"]  = result_df["is_new_high"].map(_yn)
        # 观察信号列（简洁版，详情在展开区查看）
        try:
            _sigs = [
                f"{compute_entry_signal(r)['signal_icon']} "
                f"{compute_entry_signal(r)['signal_type']}"
                for _, r in result_df.iterrows()
            ]
            disp["观察信号"] = _sigs
        except Exception:
            pass
    return disp


