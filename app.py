# ── 板块强度（供 AI 龙头 / 游资行为使用）────────────────────────────────

def compute_sector_strength(df: pd.DataFrame) -> dict:
    """
    计算各板块相对强度（0-1 归一化）。
    输入 df 需包含 code / pct_chg 列；返回 {sector: strength} 映射。
    """
    if df.empty or "pct_chg" not in df.columns:
        return {}
    try:
        tmp = df.copy()
        tmp["sector"] = tmp["code"].astype(str).apply(get_sector)
        raw: dict = {}
        for sector, grp in tmp.groupby("sector"):
            limit_cnt    = int((grp["pct_chg"] >= 9.5).sum())
            strong_cnt   = int((grp["pct_chg"] > 5).sum())
            sector_avg   = float(grp["pct_chg"].mean())
            raw[sector]  = limit_cnt * 15 + strong_cnt * 5 + sector_avg * 3
        max_s = max(raw.values()) if raw else 1
        return {k: round(v / max_s, 3) for k, v in raw.items()} if max_s > 0 else raw
    except Exception:
        return {}


# ── 5 维度高级评分 ────────────────────────────────────────────────────────

def compute_advanced_scores(row: pd.Series, sector_strength: float = 0.5) -> dict:
    """
    5 维度评分（总分 100）：
      趋势分(30) 量能分(25) 人气分(25) 板块分(10) 风险分(10)
    失败时返回全 0 默认值，不抛异常。
    """
    try:
        pct        = float(row.get("pct_chg",    0) or 0)
        turnover   = float(row.get("turnover",   0) or 0)
        vol_ratio  = float(row.get("vol_ratio")  or 0)
        amount     = float(row.get("amount",     0) or 0)
        price      = float(row.get("price",      0) or 0)
        is_limit   = bool(row.get("is_limit",   False))
        is_ma_bull = bool(row.get("is_ma_bull", False))
        is_new_high= bool(row.get("is_new_high",False))
        consec     = int(row.get("consec_limit", 0) or 0)
        ret3       = float(row.get("ret3")       or 0)

        # 趋势分 0-30
        trend = 0
        if is_ma_bull:   trend += 12
        if is_new_high:  trend += 10
        if ret3 > 8:     trend += 8
        elif ret3 > 3:   trend += 4

        # 量能分 0-25
        vol_s = 0
        if vol_ratio >= 3:      vol_s += 15
        elif vol_ratio >= 2:    vol_s += 10
        elif vol_ratio >= 1.5:  vol_s += 6
        if bool(row.get("is_heavy", False)): vol_s += 10
        vol_s = min(vol_s, 25)

        # 人气分 0-25
        pop = 0
        if is_limit:         pop += 10
        pop += min(consec * 8, 15)
        if turnover >= 10:   pop += 8
        elif turnover >= 5:  pop += 4
        if pct >= 9:         pop += 4
        elif pct >= 5:       pop += 2
        pop = min(pop, 25)

        # 板块分 0-10
        sec = min(round(sector_strength * 10), 10)

        # 风险分 0-10（越高越安全）
        risk = 10
        if price   <= 5:   risk -= 2
        if amount  < 2e8:  risk -= 3
        if consec  >= 5:   risk -= 3
        if turnover >= 20: risk -= 2
        risk = max(risk, 0)

        return {
            "trend_score":  trend,
            "vol_score":    vol_s,
            "pop_score":    pop,
            "sector_score": sec,
            "risk_score":   risk,
            "adv_total":    trend + vol_s + pop + sec + risk,
        }
    except Exception:
        return {"trend_score": 0, "vol_score": 0, "pop_score": 0,
                "sector_score": 0, "risk_score": 5, "adv_total": 5}


# ── AI 龙头识别（单股）────────────────────────────────────────────────────

def compute_ai_leader_score(row: pd.Series, sector_rank: float = 0.5) -> dict:
    """
    8 维度 AI 龙头评分（总分 ~120+）：
      涨停(25) 连板(max30) 成交额(max15) 涨幅(max10)
      换手率(max10) 量比(max10) 60日新高(10) 板块强度(max10)
    评级：S(≥80) A(≥65) B(≥50) C(≥35) D(<35)
    """
    try:
        is_limit   = bool(row.get("is_limit",    False))
        is_new_high= bool(row.get("is_new_high", False))
        consec     = int(row.get("consec_limit",  0) or 0)
        amount     = float(row.get("amount",      0) or 0)
        pct        = float(row.get("pct_chg",     0) or 0)
        turnover   = float(row.get("turnover",    0) or 0)
        vol_ratio  = float(row.get("vol_ratio")  or 0)

        score = 0
        tags: list = []

        if is_limit:
            score += 25; tags.append("涨停")

        score += min(consec * 12, 30)
        if consec >= 3:   tags.append(f"{consec}连板")
        elif consec == 2: tags.append("2连板")

        if amount >= 1e9:    score += 15; tags.append("超大单")
        elif amount >= 5e8:  score += 10; tags.append("大成交")
        elif amount >= 2e8:  score += 5

        if pct >= 9:   score += 10
        elif pct >= 7: score += 7
        elif pct >= 5: score += 4

        if turnover >= 15:   score += 10; tags.append("超高换手")
        elif turnover >= 10: score += 7;  tags.append("高换手")
        elif turnover >= 5:  score += 3

        if vol_ratio >= 4:     score += 10; tags.append("天量")
        elif vol_ratio >= 2.5: score += 7
        elif vol_ratio >= 1.5: score += 4

        if is_new_high:
            score += 10; tags.append("新高")

        sector_pts = round(sector_rank * 10)
        score += sector_pts
        if sector_rank >= 0.8: tags.append("板块龙头")

        if score >= 80:   grade, gcolor = "S", "#ff1744"
        elif score >= 65: grade, gcolor = "A", "#ff5722"
        elif score >= 50: grade, gcolor = "B", "#ff9800"
        elif score >= 35: grade, gcolor = "C", "#ffeb3b"
        else:             grade, gcolor = "D", "#78909c"

        return {
            "ai_score":       score,
            "ai_grade":       grade,
            "ai_grade_color": gcolor,
            "ai_tags":        "、".join(tags) if tags else "—",
        }
    except Exception:
        return {"ai_score": 0, "ai_grade": "D",
                "ai_grade_color": "#78909c", "ai_tags": "—"}


def run_ai_analysis(result_df: pd.DataFrame) -> pd.DataFrame:
    """
    对 result_df 批量计算 AI 龙头评分 + 5维度评分，
    返回追加了评分列的 DataFrame。任何行失败不影响其他行。
    """
    if result_df.empty:
        return result_df
    try:
        strengths = compute_sector_strength(result_df)
        rows_ai  = []
        rows_adv = []
        for _, row in result_df.iterrows():
            sector = get_sector(str(row.get("code", "")))
            rank   = strengths.get(sector, 0.5)
            rows_ai.append(compute_ai_leader_score(row, sector_rank=rank))
            rows_adv.append(compute_advanced_scores(row, sector_strength=rank))

        df = result_df.copy().reset_index(drop=True)
        ai_df  = pd.DataFrame(rows_ai)
        adv_df = pd.DataFrame(rows_adv)
        return pd.concat([df, ai_df, adv_df], axis=1)
    except Exception:
        return result_df


# ── 情绪周期 ──────────────────────────────────────────────────────────────

def compute_emotion_cycle(rt_df: pd.DataFrame) -> dict:
    """
    基于全市场行情数据计算情绪周期。
    输出 5 个阶段：强修复 / 弱修复 / 分歧 / 退潮 / 冰点
    """
    _empty = {
        "phase": "数据不足", "color": "#78909c", "score": 0,
        "desc": "请先获取行情数据",
        "limit_count": 0, "avg_pct": 0.0, "up_ratio": 0.0,
        "strong_count": 0, "down_count": 0, "total": 0,
    }
    try:
        if rt_df is None or rt_df.empty or len(rt_df) < 20:
            return _empty

        total        = len(rt_df)
        up_count     = int((rt_df["pct_chg"] > 0).sum())
        down_count   = int((rt_df["pct_chg"] < 0).sum())
        limit_count  = int((rt_df["pct_chg"] >= 9.5).sum())
        limit_down   = int((rt_df["pct_chg"] <= -9.5).sum())
        avg_pct      = float(rt_df["pct_chg"].mean())
        strong_count = int((rt_df["pct_chg"] > 5).sum())

        limit_ratio  = limit_count / total * 100
        up_ratio     = up_count    / total * 100
        strong_ratio = strong_count / total * 100

        score = (
            min(limit_ratio * 4,           40) +
            min(max(avg_pct * 4,    0),    20) +
            min(strong_ratio * 1.5,        20) +
            min(max((up_ratio - 50) * 0.4, 0), 10) +
            10
        )
        score = round(max(0.0, min(100.0, score)), 1)

        if score >= 65:
            phase, color = "强修复", "#ef5350"
            desc = (f"涨停 {limit_count} 只 | 强势股 {strong_count} 只 | "
                    f"情绪高涨，游资大举入场")
        elif score >= 45:
            phase, color = "弱修复", "#ff9800"
            desc = (f"涨停 {limit_count} 只 | 情绪偏暖，资金活跃度中等")
        elif score >= 25:
            phase, color = "分歧", "#ffeb3b"
            desc = (f"涨跌互现（{up_ratio:.0f}% 上涨）| 市场方向不明，谨慎追高")
        elif score >= 10:
            phase, color = "退潮", "#42a5f5"
            desc = (f"整体偏弱，平均涨幅 {avg_pct:+.2f}% | 资金防御为主")
        else:
            phase, color = "冰点", "#78909c"
            desc = (f"全面下跌，跌停 {limit_down} 只 | 建议空仓等待")

        return {
            "phase":        phase, "color":        color,
            "score":        score, "desc":         desc,
            "limit_count":  limit_count, "avg_pct": round(avg_pct, 2),
            "up_ratio":     round(up_ratio, 1), "strong_count": strong_count,
            "down_count":   down_count, "total": total,
            "limit_down":   limit_down,
        }
    except Exception as exc:
        result = _empty.copy()
        result["desc"] = f"计算异常：{exc}"
        return result


