# ── 板块热度 ──────────────────────────────────────────────────────────────

def compute_sector_heat(df: pd.DataFrame) -> pd.DataFrame:
    """
    热度评分 = 涨停数×30 + 平均涨幅×5 + 强势股数×10 + 成交额(亿)/10
    """
    if df.empty:
        return pd.DataFrame()
    df = df.copy()
    df["sector"] = df["code"].apply(get_sector)
    has_limit = "is_limit" in df.columns

    groups = []
    for sector, grp in df.groupby("sector"):
        limit_count  = int(grp["is_limit"].sum()) if has_limit else 0
        avg_pct      = float(grp["pct_chg"].mean())
        total_amount = float(grp["amount"].sum())
        strong_count = int((grp["pct_chg"] > 5).sum())
        heat_score   = (
            limit_count * 30
            + avg_pct * 5
            + strong_count * 10
            + total_amount / 1e9
        )
        groups.append({
            "板块":       sector,
            "涨停数":     limit_count,
            "平均涨幅":   round(avg_pct, 2),
            "强势股数":   strong_count,
            "成交额(亿)": round(total_amount / 1e8, 1),
            "股票数":     len(grp),
            "热度评分":   round(heat_score, 1),
        })

    result = pd.DataFrame(groups).sort_values("热度评分", ascending=False).reset_index(drop=True)
    result.index += 1
    return result


# ── 龙头榜排序 ────────────────────────────────────────────────────────────

def compute_leaderboard(result_df: pd.DataFrame) -> pd.DataFrame:
    """综合评分 = 游资评分×0.4 + 连板数×0.2 + 成交额×0.2 + 涨幅×0.2（各维度归一化）"""
    if result_df.empty:
        return pd.DataFrame()
    df = result_df.copy()
    weights = {"yt_score": 0.4, "consec_limit": 0.2, "amount": 0.2, "pct_chg": 0.2}
    composite = pd.Series(0.0, index=df.index)
    for col, w in weights.items():
        if col in df.columns:
            vals    = pd.to_numeric(df[col], errors="coerce").fillna(0)
            col_max = vals.max()
            if col_max > 0:
                composite += (vals / col_max) * w
    df["综合评分"] = composite.round(3)
    return df.sort_values("综合评分", ascending=False).head(20).reset_index(drop=True)


