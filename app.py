def compute_market_emotion_v4(
    rt_df: pd.DataFrame,
    result_df: pd.DataFrame | None = None,
    max_consec_override: int = 0,
) -> dict:
    """
    V4 情绪周期评分（满分100）。

    评分来源（6项，阈值见 V4_CONFIG["emotion"]）：
      涨停家数(20) 连板高度(15) 封板率(15) 炸板率(15) 跌停惩罚(15) 龙头溢价(20)

    可用字段来源：
      rt_df        全市场实时行情（pct_chg、amount）
      result_df    筛选结果含历史指标（consec_limit）
    """
    try:
        if rt_df is None or rt_df.empty or len(rt_df) < 30:
            return _empty_emotion_v4()

        cfg  = V4_CONFIG["emotion"]
        pcts = rt_df["pct_chg"].dropna()
        total_n = len(pcts)

        # ① 涨停家数（0-20分）
        limit_count   = int((pcts >= 9.5).sum())
        limit_score   = min(limit_count / max(cfg["limit_up_full"], 1) * 20, 20.0)

        # ② 连板高度（0-15分）
        max_consec = max_consec_override
        if result_df is not None and "consec_limit" in result_df.columns:
            max_consec = max(max_consec, int(result_df["consec_limit"].max() or 0))
        consec_score  = min(max_consec / max(cfg["consec_full"], 1) * 15, 15.0)

        # ③ 封板率估算（0-15分）
        # 近似：pct≥9% 视为封板，pct≥7% 视为曾触板
        near_limit = max(int((pcts >= 7.0).sum()), 1)
        seal_rate  = limit_count / near_limit
        seal_score = min(seal_rate / max(cfg["seal_full"], 0.01) * 15, 15.0)

        # ④ 炸板率（0-15分，炸板越低分越高）
        # 近似：7%≤pct<9.5% 的票 / 曾触板总数
        fry_count  = int(((pcts >= 7.0) & (pcts < 9.5)).sum())
        fry_rate   = fry_count / near_limit
        fry_score  = max(0.0, 15.0 * (1 - fry_rate / max(cfg["fry_penalty"], 0.01)))
        fry_score  = min(fry_score, 15.0)

        # ⑤ 跌停惩罚（0-15分，跌停越少越高）
        down_count = int((pcts <= -9.5).sum())
        down_score = max(0.0, 15.0 * (1 - down_count / max(cfg["limit_down_ok"], 1)))
        down_score = min(down_score, 15.0)

        # ⑥ 龙头溢价（0-20分）
        market_avg = float(pcts.mean())
        top_avg    = float(pcts.nlargest(3).mean()) if len(pcts) >= 3 else market_avg
        premium    = max(0.0, top_avg - market_avg)
        prem_score = min(premium / max(cfg["premium_full"], 0.01) * 20, 20.0)

        total = round(
            limit_score + consec_score + seal_score + fry_score + down_score + prem_score, 1
        )
        total = max(0.0, min(100.0, total))

        # 情绪阶段
        ep = V4_CONFIG["emotion_phase"]
        if total >= ep["主升"]:       phase, color = "主升",   "#ef5350"
        elif total >= ep["强修复"]:    phase, color = "强修复", "#ff7043"
        elif total >= ep["弱修复"]:    phase, color = "弱修复", "#ff9800"
        elif total >= ep["分歧"]:      phase, color = "分歧",   "#ffeb3b"
        elif total >= ep["退潮"]:      phase, color = "退潮",   "#42a5f5"
        else:                           phase, color = "冰点",   "#78909c"

        pos_range = V4_CONFIG["position"].get(phase, "0%")

        return {
            "phase":          phase,
            "color":          color,
            "total":          total,
            "position_range": pos_range,
            "limit_count":    limit_count,
            "down_count":     down_count,
            "limit_down":     down_count,
            "max_consec":     max_consec,
            "seal_rate":      round(seal_rate, 3),
            "fry_rate":       round(fry_rate, 3),
            "premium":        round(premium, 1),
            "market_avg_pct": round(market_avg, 2),
            "breakdown": {
                "涨停家数分":  round(limit_score, 1),
                "连板高度分":  round(consec_score, 1),
                "封板率分":    round(seal_score, 1),
                "炸板率分":    round(fry_score, 1),
                "跌停惩罚分":  round(down_score, 1),
                "龙头溢价分":  round(prem_score, 1),
            },
        }
    except Exception as _exc:
        r = _empty_emotion_v4()
        r["phase"] = f"计算异常: {_exc}"
        return r


# ── V4 模块二：动态龙头识别 ──────────────────────────────────────────────

def compute_leader_score_v4(row: pd.Series, sector_rank: float = 0.5) -> dict:
    """
    V4 动态龙头评分（满分100）。

    分项满分（见 V4_CONFIG["leader"]）：
      连板高度25 + 板块强度20 + 成交额15 + 换手率10 + 量比10 + 新高10 + 涨停时间10

    true_leader 判断：满足7条件中≥4条视为高可信度龙头。
    """
    try:
        cfg      = V4_CONFIG["leader"]
        consec   = int(row.get("consec_limit", 0) or 0)
        amount   = float(row.get("amount", 0) or 0)
        turnover = float(row.get("turnover", 0) or 0)
        vr       = float(row.get("vol_ratio") or 0)
        is_nh    = bool(row.get("is_new_high", False))
        is_lim   = bool(row.get("is_limit", False))
        pct      = float(row.get("pct_chg", 0) or 0)

        # ① 连板高度（0-25）
        c_sc = min(consec * 8, cfg["consec_pts"])

        # ② 板块强度（0-20）
        s_sc = round(min(sector_rank * cfg["sector_pts"], cfg["sector_pts"]))

        # ③ 成交额（0-15）
        if   amount >= 2e9:   a_sc = 15
        elif amount >= 1e9:   a_sc = 12
        elif amount >= 5e8:   a_sc = 9
        elif amount >= 2e8:   a_sc = 6
        elif amount >= 5e7:   a_sc = 3
        else:                 a_sc = 0

        # ④ 换手率（0-10；过高≥20%认为失控，上限8分）
        if   turnover >= 20:  t_sc = 8
        elif turnover >= 12:  t_sc = 10
        elif turnover >= 8:   t_sc = 8
        elif turnover >= 5:   t_sc = 5
        else:                 t_sc = max(0, int(turnover))

        # ⑤ 量比（0-10；过大≥5认为失控，上限8分）
        if   vr >= 5:    vr_sc = 8
        elif vr >= 3:    vr_sc = 10
        elif vr >= 2:    vr_sc = 7
        elif vr >= 1.5:  vr_sc = 5
        else:            vr_sc = max(0, int(vr * 2))

        # ⑥ 60日新高（0-10）
        nh_sc = cfg["new_high_pts"] if is_nh else 0

        # ⑦ 涨停时间估算（0-10；无真实涨停时间，用涨停/近涨停近似）
        lt_sc = 8 if is_lim else (4 if pct >= 7 else 0)

        total = int(c_sc + s_sc + a_sc + t_sc + vr_sc + nh_sc + lt_sc)

        # 分级
        if   total >= 85: grade, badge = "市场龙头", "🏆"
        elif total >= 75: grade, badge = "板块龙头", "⭐"
        elif total >= 60: grade, badge = "强势核心", "🔥"
        elif total >= 45: grade, badge = "跟风补涨", "📈"
        else:             grade, badge = "普通票",   "—"

        # true_leader（7条件≥4条）
        tl = [
            sector_rank >= 0.7,
            consec >= 2,
            amount >= 5e8,
            is_lim,
            5 <= turnover <= 18,
            is_nh,
            sector_rank >= 0.6,
        ]
        tl_cnt    = sum(tl)
        true_lead = tl_cnt >= 4

        return {
            "leader_score":   total,
            "leader_grade":   grade,
            "leader_badge":   badge,
            "true_leader":    true_lead,
            "tl_confidence":  round(tl_cnt / 7 * 100),
            "breakdown": {
                "连板高度": c_sc,  "板块强度": s_sc,
                "成交额":   a_sc,  "换手率":   t_sc,
                "量比":     vr_sc, "新高":     nh_sc,
                "涨停":     lt_sc,
            },
        }
    except Exception:
        return {
            "leader_score": 0, "leader_grade": "普通票", "leader_badge": "—",
            "true_leader": False, "tl_confidence": 0,
            "breakdown": {k: 0 for k in ["连板高度","板块强度","成交额","换手率","量比","新高","涨停"]},
        }


# ── V4 模块三：竞价弱转强识别 ────────────────────────────────────────────

def compute_auction_v4(row: pd.Series, emotion_total: float = 50.0) -> dict:
    """
    V4 竞价弱转强识别。
    9:25 后方有真实竞价数据；此处用实时行情近似，并标注数据来源。

    弱转强条件（满足条件越多等级越高）：
      C1 高开 2%-8%
      C2 竞价量占比估算 ≥ 5%（换手率/5 近似）
      C3 情绪周期非退潮/冰点
      C4 可能是昨弱今转强（consec=0 且今涨）
    """
    try:
        pct      = float(row.get("pct_chg",    0) or 0)
        turnover = float(row.get("turnover",   0) or 0)
        consec   = int(row.get("consec_limit", 0) or 0)

        # 近似竞价量占比
        vol_ratio_pct = min(turnover / 5.0, 1.0) if turnover else 0.0

        c1 = 2.0 <= pct <= 8.0
        c2 = vol_ratio_pct >= 0.05
        c3 = emotion_total >= 30.0
        c4 = (consec == 0 and pct > 0)
        met = sum([c1, c2, c3, c4])

        high_risk = pct >= V4_CONFIG["risk"]["open_high"]

        if   met >= 3 and not high_risk: grade, watch, risk_chase = "强", True,  False
        elif met >= 2 and not high_risk: grade, watch, risk_chase = "中", True,  False
        elif met >= 1:                   grade, watch, risk_chase = "弱", False, high_risk
        else:                            grade, watch, risk_chase = "—",  False, False

        return {
            "auction_pct":       round(pct, 2),
            "vol_ratio_pct":     round(vol_ratio_pct * 100, 1),
            "wts_grade":         grade,
            "watchable":         watch,
            "high_risk_chase":   risk_chase,
            "auction_score":     min(met * 25, 100),
            "conditions_met":    met,
        }
    except Exception:
        return {
            "auction_pct": 0, "vol_ratio_pct": 0,
            "wts_grade": "—", "watchable": False,
            "high_risk_chase": False, "auction_score": 0, "conditions_met": 0,
        }


# ── V4 模块四：风险控制系统 ──────────────────────────────────────────────

def compute_risk_score_v4(row: pd.Series, emotion_total: float = 50.0) -> dict:
    """
    V4 风险评分（0-100，越高越危险）。

    风险因子（见 V4_CONFIG["risk"]）：
      连板过高(20) 放量未封板(20) 高开追高(15) 异常放量(15) 情绪退潮(10)

    auto_degrade：触发任一降级条件时为 True，推荐等级自动下调。
    """
    try:
        cfg    = V4_CONFIG["risk"]
        consec = int(row.get("consec_limit",  0) or 0)
        pct    = float(row.get("pct_chg",     0) or 0)
        vr     = float(row.get("vol_ratio")  or 0)
        turn   = float(row.get("turnover",    0) or 0)
        is_lim = bool(row.get("is_limit",    False))

        risk    = 0
        reasons: list = []

        # ① 连板过高
        if consec >= cfg["consec_danger"]:
            risk += 20; reasons.append(f"{consec}连板高位回调风险大")
        elif consec >= 4:
            risk += 10; reasons.append(f"{consec}连板注意高位风险")

        # ② 放量未封板（潜在炸板）
        if vr > cfg["vol_explosion"] and not is_lim:
            risk += 20; reasons.append("异常放量未封板（疑似炸板）")
        elif vr > 3.0 and not is_lim:
            risk += 10; reasons.append("放量未涨停")

        # ③ 高开追高
        if pct >= cfg["open_high"]:
            risk += 15; reasons.append(f"当前涨幅{pct:.1f}%，追高风险高")
        elif pct >= 5.0:
            risk += 8

        # ④ 异常放量
        if vr >= cfg["vol_explosion"]:
            risk += 15; reasons.append(f"量比{vr:.1f}x，资金博弈激烈")

        # ⑤ 情绪惩罚
        if emotion_total < 15:
            risk += 10; reasons.append("市场处于冰点")
        elif emotion_total < 30:
            risk += 5;  reasons.append("市场情绪退潮")

        risk = int(min(risk, 100))

        # 自动降级触发条件
        auto_degrade = any([
            emotion_total < 30,
            pct >= cfg["open_high"],
            consec >= cfg["consec_danger"],
            (vr >= cfg["vol_explosion"] and not is_lim),
        ])

        if   risk <= 30: level, color = "低风险",   "#26a69a"
        elif risk <= 60: level, color = "中风险",   "#ff9800"
        elif risk <= 80: level, color = "高风险",   "#ef5350"
        else:            level, color = "极高风险", "#b71c1c"

        return {
            "risk_score":   risk,
            "risk_level":   level,
            "risk_color":   color,
            "auto_degrade": auto_degrade,
            "risk_reasons": reasons,
        }
    except Exception:
        return {
            "risk_score": 30, "risk_level": "中风险", "risk_color": "#ff9800",
            "auto_degrade": False, "risk_reasons": [],
        }


# ── V4 模块五：最终综合评分 ──────────────────────────────────────────────

def compute_final_composite_v4(
    leader_score:  float,
    sector_score:  float,
    vol_score:     float,
    pop_score:     float,
    auction_score: float,
    risk_score:    float,
    emotion_total: float = 50.0,
) -> dict:
    """
    最终综合评分（0-100）。
    权重见 V4_CONFIG["composite"]。
    情绪越差整体打折越多；高风险自动降级。
    """
    try:
        cfg = V4_CONFIG["composite"]
        raw = (
            leader_score  * cfg["leader_w"]
            + sector_score * cfg["sector_w"]
            + vol_score    * cfg["vol_w"]
            + pop_score    * cfg["pop_w"]
            + auction_score * cfg["auction_w"]
            - risk_score   * cfg["risk_deduct"]
        )
        # 情绪折扣
        if   emotion_total < 15: raw *= 0.60
        elif emotion_total < 30: raw *= 0.75
        elif emotion_total < 45: raw *= 0.88

        score = round(max(0.0, min(raw, 100.0)), 1)

        g = V4_CONFIG["grade"]
        if   score >= g["重点关注"]: grade, color = "重点关注", "#ef5350"
        elif score >= g["可观察"]:   grade, color = "可观察",   "#ff9800"
        elif score >= g["备选"]:     grade, color = "备选",     "#ffeb3b"
        elif score >= g["谨慎"]:     grade, color = "谨慎",     "#78909c"
        else:                        grade, color = "剔除",     "#455a64"

        return {
            "composite_score": score,
            "recommend_grade": grade,
            "recommend_color": color,
        }
    except Exception:
        return {"composite_score": 0.0, "recommend_grade": "剔除", "recommend_color": "#455a64"}


